const htmlRoot = document.getElementById('html-root');
const languageToggle = document.getElementById('language-toggle');

let currentLanguage = localStorage.getItem('landing-language') || 'th';

function setLanguage(language) {
  currentLanguage = language;
  localStorage.setItem('landing-language', language);

  htmlRoot.lang = language;

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.dataset.i18n;
    const value = translations[language][key];

    if (value) {
      element.textContent = value;
    }
  });

  languageToggle.textContent = language === 'th' ? 'EN' : 'TH';
}

languageToggle.addEventListener('click', () => {
  setLanguage(currentLanguage === 'th' ? 'en' : 'th');
});

setLanguage(currentLanguage);

const revealElements = document.querySelectorAll('.reveal');

revealElements.forEach((element) => {
  element.classList.add(
    'opacity-0',
    'translate-y-8',
    'transition-all',
    'duration-700',
    'ease-out'
  );
});

const revealObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;

      entry.target.classList.remove('opacity-0', 'translate-y-8');
      entry.target.classList.add('opacity-100', 'translate-y-0');

      revealObserver.unobserve(entry.target);
    });
  },
  {
    threshold: 0.16,
  }
);

revealElements.forEach((element) => revealObserver.observe(element));

window.addEventListener('scroll', () => {
  const scrollY = window.scrollY;
  const heroCard = document.querySelector('#home .shadow-glow');

  if (!heroCard) return;

  heroCard.style.transform = `translateY(${scrollY * 0.04}px) scale(${1 - scrollY * 0.00004})`;
});